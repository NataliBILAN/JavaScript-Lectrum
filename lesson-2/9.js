////////////////// Решение //////////////////
const arr = [1, 2, 3, 4, 5, 6];
for (let i = 0; i < arr.length; i++) {    
    for (let k = 0; k < i; k++) {
        if (arr[i] > arr[k]) {
            let b = arr[i];
            arr[i] = arr[k];
            arr[k] = b;
        }
    }
}

